#!/usr/bin/env node
/**
 * Skill Generator
 * @module skill-creator/generator
 * @version 1.6.0
 *
 * Generates skill scaffolding with proper classification and eval setup.
 * ENH-97: Integrated skill creation workflow.
 */

const fs = require('fs');
const path = require('path');

const TEMPLATES_DIR = path.join(__dirname, 'templates');
const SKILLS_DIR = path.join(__dirname, '..', 'skills');
const EVALS_DIR = path.join(__dirname, '..', 'evals');

/**
 * Generate a new skill scaffold
 * @param {Object} options
 * @param {string} options.name - Skill name
 * @param {string} options.classification - workflow | capability | hybrid
 * @param {string} [options.description] - Skill description
 * @param {string} [options.argumentHint] - Argument hint
 */
function generateSkill(options) {
  const { name, classification, description, argumentHint } = options;

  if (!name || !classification) {
    console.error('Usage: node generator.js --name <name> --classification <workflow|capability|hybrid>');
    process.exit(1);
  }

  // 1. Create skill directory
  const skillDir = path.join(SKILLS_DIR, name);
  if (!fs.existsSync(skillDir)) {
    fs.mkdirSync(skillDir, { recursive: true });
  }

  // 2. Load and populate template
  const templateFile = `${classification}-skill.yaml`;
  const templatePath = path.join(TEMPLATES_DIR, templateFile);

  if (!fs.existsSync(templatePath)) {
    console.error(`Template not found: ${templateFile}`);
    process.exit(1);
  }

  let template = fs.readFileSync(templatePath, 'utf8');
  template = template.replace(/\{name\}/g, name);
  template = template.replace(/\{description\}/g, description || `${name} skill`);
  template = template.replace(/\{argument-hint\}/g, argumentHint || '');

  // 3. Write SKILL.md
  const skillContent = `---\n${template}---\n\n# ${name}\n\n> Generated by Skill Creator v1.6.1\n\n## Description\n\n${description || 'TODO: Add description'}\n`;
  fs.writeFileSync(path.join(skillDir, 'SKILL.md'), skillContent);

  // 4. Create eval directory
  const evalDir = path.join(EVALS_DIR, classification, name);
  if (!fs.existsSync(evalDir)) {
    fs.mkdirSync(evalDir, { recursive: true });
  }

  // 5. Create eval.yaml from template
  let evalTemplate = fs.readFileSync(path.join(TEMPLATES_DIR, 'eval-template.yaml'), 'utf8');
  evalTemplate = evalTemplate.replace(/\{name\}/g, name);
  evalTemplate = evalTemplate.replace(/\{classification\}/g, classification);

  if (classification === 'capability' || classification === 'hybrid') {
    evalTemplate = evalTemplate.replace('enabled: false', 'enabled: true');
  }

  fs.writeFileSync(path.join(evalDir, 'eval.yaml'), evalTemplate);

  // 6. Create prompt and expected files
  fs.writeFileSync(
    path.join(evalDir, 'prompt-1.md'),
    `# Test Prompt for ${name}\n\nTest the ${name} skill trigger accuracy and output quality.\n`
  );

  fs.writeFileSync(
    path.join(evalDir, 'expected-1.md'),
    `# Expected Output for ${name}\n\nExpected: Skill triggers correctly and produces appropriate output.\n`
  );

  console.log(`Skill '${name}' created successfully:`);
  console.log(`  Skill: ${path.join(skillDir, 'SKILL.md')}`);
  console.log(`  Eval:  ${evalDir}/`);
  console.log(`  Classification: ${classification}`);
}

// CLI execution
if (require.main === module) {
  const args = process.argv.slice(2);
  const flags = {};

  for (let i = 0; i < args.length; i++) {
    if (args[i].startsWith('--')) {
      flags[args[i].slice(2)] = args[i + 1] || true;
      i++;
    }
  }

  generateSkill({
    name: flags.name,
    classification: flags.classification || 'capability',
    description: flags.description,
    argumentHint: flags['argument-hint']
  });
}

module.exports = { generateSkill };
